//  Printing Decimal, Hexadecimal and Octal Numbers 
//
/////////////////////////////////////////////////////////

#include<stdio.h>

int main()
{
  int iNo = 97;

  printf("Decimal Number is : %d\n",iNo);
  printf("Hexadecimal Number is : %x\n",iNo);
  printf("Octal Number is : %o\n",iNo);
  return 0;
}
